package Review.java;

public class whileLoop {

	public static void main(String[] args) {
		
		int numStars = 0;
		int numPrinted = 0;
		
		
		numStars = 12;
		
		numPrinted = 1;
		
		while (numPrinted <= numStars){
			
			System.out.print(" * ");
			
			numStars --;
			
		}
		
		System.out.println(" ");
	}

}
